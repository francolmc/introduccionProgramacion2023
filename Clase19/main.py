# Definir un arreglo
mi_arreglo = [1, 2, 3]
# Definir arreglo con distintos tipos
mi_arreglo2 = [1, 2, 3, 'Pedro']

# Acceder a un elemento del arreglo
print('Obtener un elemento del arreglo: ', mi_arreglo[1])

# Modificar o asignar un elemento a una posicion del arreglo
mi_arreglo[2] = 8

# Print arreglos
print(mi_arreglo)
print(mi_arreglo2)

# Recorrer arreglos
for elemento in mi_arreglo:
    print(elemento)

for elemento in mi_arreglo2:
    print(elemento)
